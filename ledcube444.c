void ys(uint8_t i) // 1ms延时
{
  uint8_t x, j;
  for (j = 0; j < i; j++)
    for (x = 0; x <= 148; x++)
      ;
}

void init() //初始化函数
{
  TMOD = 0x02; //定时器0，工作模式2（0000，0010），8位定时模式
  TH0 = 0x06; //写入预置初值6到定时器0，使250微秒溢出一次（12MHz）
  TL0 = 0x06; //写入预置值
  ET0 = 1;    //允许定时器0中断
  EA = 1;     //允许总中断
}

