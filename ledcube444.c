void delay(uint8_t z) {
  uint8_t x, y;
  for (x = 80; x > 0; x--)
    for (y = z; y > 0; y--)
      ;
}
void light1(uint8_t num1) {
  uint8_t j;
  P0 = 0x00;
  P2 = 0x00;
  j = table[num1];
  delay(j);
  P0 = 0xff;
  P2 = 0x00;
  delay(150 - j);
}

void light2(uint8_t num2) {
  uint8_t j;
  P0 = 0xff;
  P2 = 0x00;
  j = table[num2];
  delay(j);
  P0 = 0x00;
  P2 = 0x00;
  delay(150 - j);
}
